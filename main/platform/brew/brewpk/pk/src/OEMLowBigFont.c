static int
OEMFont_MeasureText(IFont *pIFont, const AECHAR *pwcText, int cwText,
					int nMaxWidth, int *pnFits, int *pnPixels)
{
	OEMFont *me = MYOBJECT;
	int xWidth = me->xWid;
	int cntHalf = me->nHalfChars;
	int wid = 0;
	int ii;
	for (ii = 0; ii < cwText; ++ii) {
		int widChar = CHAR_WIDTH(pwcText[ii], cntHalf, xWidth, me->cls);
		wid += widChar;
		if (wid > nMaxWidth) {
			wid -= widChar;
			break;
		}
	}
	*pnFits = ii;
	*pnPixels = wid;
	return SUCCESS;
}



