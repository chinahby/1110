// Find the x & y of the character cell
__inline void
OEMFont_LocateGlyph(OEMFont *me, uint16 wChar, int *pxChar, int *pyChar)
{
	uint16 wGlyph;
	unsigned char low, high;
	int i = 0;
// LZhao added for the unicode font lib, in this case, calculate the coordination of each character by the unicode. Dec 8th, 2009.
#ifdef FEATURE_CN_UNICODE_FONT_LIB
   int m = 0;
   //wChar = (wChar >> 8) | ((wChar & 0xFF) << 8);
   if (wChar & 0xFF00) {
                
      int x = 0, y = 0;                
      if(wChar >= 0x4e00 
               	&& wChar <= 0x4ebd) // the first special line, with some special dealing.
      {
          x = 0x0100 - (0x4ebd - 0x4e00 + 1) + (wChar - 0x4e00);
          y = 3;      //  the third special line.
      }else
      {
          x = (wChar-font_map[m].unicode_start) % FONT_PER_LINE;
          y = (wChar-font_map[m].unicode_start) / FONT_PER_LINE + font_map[m].row;
      }
      //low 8 bits of wGlyph is col index of wChar in the bitmap
      //high 8 bits of wGlyph is row index of wChar in the bitmap
      wGlyph = (x&0xFF) + ((y&0xFF)<<8);                          
                
   } else {
      // ASCII
      wGlyph = wChar;
      //wGlyph=(wChar>>8)-1;
   }

#else/*!FEATURE_CN_UNICODE_FONT_LIB*/
   wChar = (wChar >> 8) | ((wChar & 0xFF) << 8);
   if (wChar & 0xFF) {
      // Chinese
      UC16_to_GB ((unsigned char*)&wChar, 1, (unsigned char*)&wGlyph);
      low = wGlyph >> 8;
      high = (wGlyph & 0xFF);
      wGlyph=(wGlyph>>8)|((wGlyph&0xFF)<<8);
      if(wGlyph>=0xB0A1)
         wGlyph=(high-164)*94+low-9;
      else {
         wGlyph = (high -160) * 95 + low;
         i = high - 0xa1;
      }
   } else {
      // ASCII
      wGlyph = (wChar >> 8);
      //wGlyph=(wChar>>8)-1;
   }
			
			
#endif/*FEATURE_CN_UNICODE_FONT_LIB*/

	*pxChar = ((wGlyph & 0xFF) - i) * me->xWid;
	*pyChar = (wGlyph >> 8) * me->yHgt;
        
}

