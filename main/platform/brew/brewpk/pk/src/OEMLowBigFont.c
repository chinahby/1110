static int
OEMFont_DrawText(IFont *pMe, IBitmap *pDst, int x, int y, const AECHAR *pcText, int nChars,
                 NativeColor foreground, NativeColor background, const AEERect *prcClip, uint32 dwFlags)
{
	
	
	OEMFont *me = (OEMFont *)pMe;
	AEERasterOp rop = AEE_RO_COPY;
	int xMin, xMax, yMin, yMax;
	int xWidth = me->xWid;
	int cntHalf = me->nHalfChars;
	int yHeight = me->yHgt;
	int yOffset = 0; // y index into character bitmap
	int nResult = SUCCESS;
	int ii;

	if(NULL == pMe)
	{
		return EFAILED;
	}
    
	OEMFont_SetPalette(me, pDst, foreground, background);
    
	if (dwFlags & IDF_TEXT_TRANSPARENT) {
		rop = AEE_RO_TRANSPARENT;
	}
    
	xMin = prcClip->x;
	yMin = prcClip->y;
	xMax = xMin + prcClip->dx;
	yMax = yMin + prcClip->dy;
	// Clip bottom of line
	if (y+yHeight > yMax) {
		yHeight = yMax - y;
	}
	// Clip top of line
	if (y < yMin) {
		yOffset = yMin - y;
		yHeight -= yOffset;
		y = yMin;
	}
	if (yHeight <= 0) {
		return SUCCESS; // don't call BltIn() with negative height (just to be nice)
	}
	// Draw characters
	//
	// Invariants:
	// ii = array index of next character
	// x = x coordinate of next character
	// skip chars clipped on left
	for (ii = 0; ii < nChars; ++ii) {
		int widChar = CHAR_WIDTH(pcText[ii], cntHalf, xWidth, me->cls);
		x += widChar;
		if (x > xMin) {
			x -= widChar;
			break;
		}
	}
	for (; ii < nChars && x < xMax; ++ii) {
		int xChar, yChar;
		AECHAR wch = pcText[ii];
		int widChar = CHAR_WIDTH(pcText[ii], cntHalf, xWidth, me->cls);
        OEMFont_LocateGlyph(me, wch, &xChar, &yChar);
		// Clip left side of character
		if (x < xMin) {
			xChar += xMin - x;
			widChar -= xMin - x;
			x = xMin;
		}
		// Clip right side of character
		if (x + widChar > xMax) {
			widChar = xMax - x;
		}
		nResult = IBITMAP_BltIn(pDst, x, y, widChar, yHeight, 
                                                (IBitmap*)me->pdib /*me->pbitmap*/
                                                ,xChar, yChar+yOffset, rop);
		if (nResult != SUCCESS)
			break;
		x += widChar;
	}
	return nResult;
}



