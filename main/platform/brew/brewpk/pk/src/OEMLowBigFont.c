static uint32 OEMFont_Release(IFont *pme)
{
    OEMFont *me = (OEMFont *)pme;
    
    if (!me->uRefs)
        return 0;

    if (--me->uRefs > 0)
        return me->uRefs;

    // Ref count is zero, so clean up
    if (me->pdib)
        IDIB_Release(me->pdib);
    
    FREE(me);   
    return 0;
}


