static int get_char_wid(AECHAR wchar, int half_wid_cnt, int full_wid, AEECLSID cls)
{
    int i = 0, m = 0;
    int wid = full_wid; //default

    // unicode?
    if(wchar >= half_wid_cnt)
        return wid;

    //ascii wid lookup
    for(; i < font_wid_info_count; i++)
    {
        if(font_wid_infos[i].cls == cls)
        {			
        	for(m = 0; m < font_wid_infos[i].count; m++)
            {
            	if(font_wid_infos[i].wid_info[m].wchar == wchar)
            	{
            		wid = font_wid_infos[i].wid_info[m].width;
            		return wid;
            	}
        	}

			break;
        }
    }

	//default width for ascii is half of full width
	wid = (full_wid>>1);

    return wid;
}
