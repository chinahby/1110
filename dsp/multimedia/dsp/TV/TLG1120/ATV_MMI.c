void TLGAPP_InitHsyncAndVsync(void)
{
	uint16 v_hi_start1, v_hi_stop1, v_hi_start2, v_hi_stop2, v_hi_start3, v_hi_stop3;
	uint16 h_hi_start, h_hi_stop, h_kill_start1, h_kill_stop1, h_kill_start2, h_kill_stop2;

	switch(gTvScanLines)
	{
		case TLG_TV_525_LINES:
			h_hi_start 	= 139+(H_OFFSET_525+gTvOptions.mHOffset)*2+(H_REDUCE_525+gTvOptions.mHReduce);
			h_hi_stop 	= 858+(H_OFFSET_525+gTvOptions.mHOffset)*2-(H_REDUCE_525+gTvOptions.mHReduce)-H_OFFSET_RANGE*2;
			h_kill_start1	= 1;
			h_kill_stop1 	= 19+(V_OFFSET_525+gTvOptions.mVOffset)+(V_REDUCE_525+gTvOptions.mVReduce+1)/2;
			h_kill_start2 	= 264+(V_OFFSET_525+gTvOptions.mVOffset)-(V_REDUCE_525+gTvOptions.mVReduce)/2-V_OFFSET_RANGE;
			h_kill_stop2	= 525;
			v_hi_start1 	= 1;
			v_hi_stop1 	= 19+(V_OFFSET_525+gTvOptions.mVOffset)+(V_REDUCE_525+gTvOptions.mVReduce);
			v_hi_start2 	= 0;
			v_hi_stop2 	= 0;
			v_hi_start3 	= 0;
			v_hi_stop3 	= 0;
			break;

		case TLG_TV_625_LINES:
			h_hi_start 	= 145+(H_OFFSET_625+gTvOptions.mHOffset)*2+(H_REDUCE_625+gTvOptions.mHReduce);
			h_hi_stop 	= 864+(H_OFFSET_625+gTvOptions.mHOffset)*2-(H_REDUCE_625+gTvOptions.mHReduce)-H_OFFSET_RANGE*2;
			h_kill_start1	= 1;
			h_kill_stop1 	= 22+(V_OFFSET_625+gTvOptions.mVOffset)+(V_REDUCE_625+gTvOptions.mVReduce+1)/2;
			h_kill_start2 	= 311+(V_OFFSET_625+gTvOptions.mVOffset)-(V_REDUCE_625+gTvOptions.mVReduce)/2-V_OFFSET_RANGE;
			h_kill_stop2	= 625;
			v_hi_start1 	= 1;
			v_hi_stop1 	= 22+(V_OFFSET_625+gTvOptions.mVOffset)+(V_REDUCE_625+gTvOptions.mVReduce);
			v_hi_start2 	= 0;
			v_hi_stop2 		= 0;
			v_hi_start3 	= 0;
			v_hi_stop3 		= 0;
			break;

		default:
#ifdef WIN32
			;
#else
			TLG_PRINT_0("ATV ERROR: Unsupported scan lines!\n");
#endif
	}
	
	TLG_SetExtHSync(tlg_i2c_addr, h_hi_start, h_hi_stop, h_kill_start1, h_kill_stop1, h_kill_start2, h_kill_stop2);
	TLG_SetExtVSync(tlg_i2c_addr, v_hi_start1, v_hi_stop1, v_hi_start2, v_hi_stop2, v_hi_start3, v_hi_stop3);

#ifdef WIN32
	printf("TLG_SetExtHSync(tlg_i2c_addr, %d, %d, %d, %d, %d, %d);\n", h_hi_start, h_hi_stop, h_kill_start1, h_kill_stop1, h_kill_start2, h_kill_stop2);
	printf("TLG_SetExtVSync(tlg_i2c_addr, %d, %d, %d, %d, %d, %d);\n", v_hi_start1, v_hi_stop1, v_hi_start2, v_hi_stop2, v_hi_start3, v_hi_stop3);
	printf(" TV window: w=%d, h=%d\n", (h_hi_stop - h_hi_start + 1) / 2, h_kill_start2 - h_kill_stop1 - 1);
#endif
}

